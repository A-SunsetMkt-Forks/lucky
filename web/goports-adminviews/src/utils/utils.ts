const MenuIndexList = ["#status","#log","#relayset","#whitelistset","#whitelists","#blacklists","#set","#login"]

export function PageExist(page:string) {
    for(let i in MenuIndexList){
        if (MenuIndexList[i]==page){
            return true
        }
    }
    return false
}

